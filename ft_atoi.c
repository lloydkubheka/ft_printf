#include "ft_printf.h"

int				ft_atoi_f(char **s)
{
	int			ret;
	int			c;
	int			sign;

	ret = 0;
	c = 0;
	sign = 1;
	while ('0' <= (*s)[c] && (*s)[c] <= '9')
	{
		ret = ret * 10 + (*s)[c] - '0';
		c++;
	}
	(*s) = &(*s)[c];
	return (ret * sign);
}
