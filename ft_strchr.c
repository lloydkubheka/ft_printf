#include "ft_printf.h"

char			ft_strchr_f(char *s, int c)
{
	char		*temp;

	temp = s;
	while (*temp != '\0')
	{
		if (*temp == c)
			return (1);
		temp++;
	}
	if (*temp == '\0' && c == '\0')
		return (0);
	return (0);
}
